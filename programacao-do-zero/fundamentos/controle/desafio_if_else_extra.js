// Inserir if else ... else if.
const nota = -1;

if (nota <= 10 && nota >= 9) {
  console.log("A");
} else if (nota < 9 && nota >= 7) {
  console.log("B");
} else if (nota < 7 && nota >= 5) {
  console.log("C");
} else if (nota < 5 && nota >= 4.5) {
  console.log("D");
} else {
  console.log("F");
}
